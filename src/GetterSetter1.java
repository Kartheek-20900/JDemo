
public class GetterSetter1 {

	int a;
	String name;
	GetterSetter1(int a,String name){
		this.a=a;
		this.name=name;
	}
	public int getA() {
		return a;
	}
	public void setA(int b) {
		this.a = b;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}

	
